print('한국', 'korea', 2023)
print('한국', 'korea', 2023, sep='-') #구분자 (-)넣어서 출력
print('한국', 'korea', 2023, end='@') #문장 끝에 @출력
print('\n') #한 줄 띄우기
print('python', 'language');print('Hi python')
print('python', 'language', end=' ');print('Hi python') #줄 바꿈 없이 출력하기
print('"Hi python"') #큰 따옴표 출력
print("'Hi python'") #작은 따옴표 출력
print("\'Hi python\'") #작은 따옴표 출력
print('Hi python '*5) #5번 반복 출력 
print("hi \n python") #\n 은 줄바꿈
print('\n 포맷 활용') #한줄 띄우고 문자열 출력