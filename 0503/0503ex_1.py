# for 반복문
for i in range(1,11):   # 반복 조건
    print(i,end=' ')    # i를 출력, end를 써서 공백을 넣고 연결되도록 함



print()
# while 반복문
i=1 # i (반복횟수) 초기화 먼저 해줌
while i<=10:    # 반복 조건
    print(i,end=' ')    # i를 출력, end를 써서 공백을 넣고 연결되도록 함
    i=i+1   # i를 1씩 증가시켜서 출력